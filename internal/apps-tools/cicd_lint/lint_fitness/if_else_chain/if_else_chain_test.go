// srcConsecutiveIfXIntNoExit is reused by multiple tests that need a simple consecutive-if violation.
const srcConsecutiveIfXIntNoExit = `package p
func f(x int) {
	if x > 0 {
		doA()
	}
	if x < 0 {
		doB()
	}
}
`

// writeGoFile creates a temporary Go source file with the given content and returns its path.
func writeGoFile(t *testing.T, content string) string {
	t.Helper()

	dir := t.TempDir()
	path := filepath.Join(dir, "sample.go")

	require.NoError(t, os.WriteFile(path, []byte(content), cryptoutilSharedMagic.KeyFilePermissions))

	return path
}

