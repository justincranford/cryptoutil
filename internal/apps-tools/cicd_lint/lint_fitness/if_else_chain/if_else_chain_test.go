func TestCheckFile_NoViolation_SelectorFieldAccumulator(t *testing.T) {
	t.Parallel()

	// Filter-accumulator pattern: each if tests a different field of the same struct.
	// These should NOT be flagged because the conditions are independent (different fields).
	src := `package p
func applyFilters(db *DB, f *Filters) *DB {
	if f.Name != nil {
		db = db.Where("name=?", *f.Name)
	}
	if f.Status != nil {
		db = db.Where("status=?", *f.Status)
	}
	if f.Active != nil {
		db = db.Where("active=?", *f.Active)
	}
	return db
}
`
	violations, err := lintFitnessIfElseChain.CheckFile(writeGoFile(t, src))
	require.NoError(t, err)
	require.Empty(t, violations, "independent filter-accumulator patterns should not be flagged")
}

func TestCheckFile_Violation_SameSelectorField(t *testing.T) {
	t.Parallel()

	// Both conditions test the SAME field (f.Status) — this is a violation.
	src := `package p
func checkStatus(f *Filters) {
	if f.Status == "active" {
		doActive()
	}
	if f.Status == "pending" {
		doPending()
	}
}
`
	violations, err := lintFitnessIfElseChain.CheckFile(writeGoFile(t, src))
	require.NoError(t, err)
	require.Len(t, violations, 1)
	require.Contains(t, violations[0].Message, "f.Status")
}

func TestCheckFile_NoViolation_IfElseWithReturn(t *testing.T) {
	t.Parallel()

	// An if/else where both branches return — then a following if is not a violation
	// because the trailing if/else chain always exits.
	src := `package p
import "errors"
func f(x int) error {
	if x > 0 {
		return errors.New("positive")
	} else {
		return errors.New("non-positive")
	}
	if x == 0 {
		return errors.New("zero")
	}
	return nil
}
`
	violations, err := lintFitnessIfElseChain.CheckFile(writeGoFile(t, src))
	require.NoError(t, err)
	require.Empty(t, violations, "if/else that always exits should not trigger violation on next if")
}
