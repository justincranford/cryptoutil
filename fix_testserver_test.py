    "// captureTB wraps *testing.T to record Fatalf calls without exiting the test.\n"
    "// Used to exercise error code paths in StartAndWait that call t.Fatalf.\n"
    "type captureTB struct {\n"
    "\t*testing.T\n"
    "\tmu       sync.Mutex\n"
    "\tfatals   []string\n"
    "\tcleanups []func()\n"
    "}\n"
    "\n"
    "// Helper is a no-op override so captureTB call frames are not marked as test helpers.\n"
    "func (c *captureTB) Helper() {}\n"
    "\n"
    "// Fatalf records the fatal message without calling runtime.Goexit.\n"
    "func (c *captureTB) Fatalf(format string, args ...any) {\n"
    "\tc.mu.Lock()\n"
    "\tc.fatals = append(c.fatals, fmt.Sprintf(format, args...))\n"
    "\tc.mu.Unlock()\n"
    "}\n"
    "\n"
    "// Cleanup captures cleanup functions instead of registering them with *testing.T.\n"
    "func (c *captureTB) Cleanup(f func()) {\n"
    "\tc.mu.Lock()\n"
    "\tc.cleanups = append(c.cleanups, f)\n"
    "\tc.mu.Unlock()\n"
    "}\n"
    "\n"
    "func (c *captureTB) runCleanups() {\n"
    "\tc.mu.Lock()\n"
    "\tfns := make([]func(), len(c.cleanups))\n"
    "\tcopy(fns, c.cleanups)\n"
    "\tc.mu.Unlock()\n"
    "\n"
    "\tfor i := len(fns) - 1; i >= 0; i-- {\n"
    "\t\tfns[i]()\n"
    "\t}\n"
    "}\n"
    "\n"
    "func (c *captureTB) hasFatal() bool {\n"
    "\tc.mu.Lock()\n"
    "\tdefer c.mu.Unlock()\n"
    "\n"
    "\treturn len(c.fatals) > 0\n"
    "}\n"
