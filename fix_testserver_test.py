    "func (m *mockServer) Start(_ context.Context) error {\n"
    "\tif m.startDelay > 0 {\n"
    "\t\ttime.Sleep(m.startDelay)\n"
    "\t}\n"
    "\n"
    "\tif m.startErr != nil {\n"
    "\t\treturn m.startErr\n"
    "\t}\n"
    "\n"
    "\tm.mu.Lock()\n"
    "\tm.startCalled = true\n"
    "\tm.publicPort = 8080\n"
    "\tm.adminPort = 9090\n"
    "\tm.mu.Unlock()\n"
    "\n"
    "\treturn nil\n"
